  <p class="text-center h2">What is an drone?</p>
  <p class="h3 text-center">
  A drone, in technological terms, is an unmanned aircraft. Drones are more formally known as unmanned aerial vehicles (UAVs) or unmanned aircraft systems (UASes). Essentially, a drone is a flying robot that can be remotely controlled or fly autonomously through software-controlled flight plans in their embedded systems, working in conjunction with onboard sensors and GPS.
  <p class="text-center h2">The Best Small Drones</p>
  <p class="h3 text-center">
  For a long time, the DJI Phantom series was about as small as you could go if you wanted to get a full-featured drone that maintains stability in the air and includes strong safety features. That's no longer the case. Hikers and travel photographers appreciate a small, light kit, and they can now get a drone that fits into a backpack.</p>

  <p class="text-center h2">What Are the Best Brands of Drones?</p>
  <p class="h3 text-center">
  DJI models currently dominate our top picks, and there's a good reason for that. The company is simply a few steps ahead of its competition right now, and has a product catalog with models at various price points, which take up a good number of the slots in our top ten. It made a huge splash with its iconic Phantom series, and now makes the best small drones we've tested in the form of the Mavic series.</p>

  <p class="text-center h2">Racing and Toy Drones</p>
  <p class="h3 text-center">
  There are a number of products on the market that are sold as drones, but don't quite fit the bill. Remote-controlled aircraft have been around for ages. But with the recent surge in popularity, quadcopters that would simply be sold as RC products are now being tagged as drones. These don't include GPS stabilization, return-to-home functionality, and other automated flight modes that make a drone a drone. We also don't review many of them.</p>
  <p class="text-center h2">History</p>
  <p class="h3 text-center">
  Many trace the history of drones to 1849 Italy, when Venice was fighting for its independence from Austria. Austrian soldiers attacked Venice with hot-air, hydrogen- or helium-filled balloons equipped with bombs. The first pilotless radio-controlled aircraft were used in World War I. In 1918, the U.S. Army developed the experimental Kettering Bug, an unmanned "flying bomb" aircraft, which was never used in combat. The first generally used drone appeared in 1935 as a full-size retooling of the de Havilland DH82B "Queen Bee" biplane, which was fitted with a radio and servo-operated controls in the back seat. The plane could be conventionally piloted from the front seat, but generally it flew unmanned and was shot at by artillery gunners in training. The term drone dates to this initial use, a play on the "Queen Bee" nomenclature. UAV technology continued to be of interest to the military, but it was often too unreliable and costly to put into use. After concerns about the shooting down of spy planes arose, the military revisited the topic of unmanned aerial vehicles. Military use of drones soon expanded to play roles in dropping leaflets and acting as spying decoys. Military droneGETTY Soldiers of the Bundeswehr, the German armed forces, recover a KZO reconnaissance drone following a successful mission during Thunder Storm 2018 multinational NATO military exercises on June 7, 2018 near Pabrade, Lithuania. Military drone use solidified in 1982 when the Israeli Air Force used UAVs to wipe out the Syrian fleet with minimal loss of Israeli forces. The Israeli UAVs acted as decoys, jammed communication and offered real-time video reconnaissance. Drones have continued to be a mainstay in the military, playing critical roles in intelligence, surveillance and force protection, artillery spotting, target following and acquisition, battle damage assessment and reconnaissance, as well as for weaponry.</p>
